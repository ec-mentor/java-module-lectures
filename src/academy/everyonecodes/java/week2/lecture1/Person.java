package academy.everyonecodes.java.week2.lecture1;

public class Person {
    // Properties or Fields or instance variables (ivars)
    private String name;
    private int age;

    public Person(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

}
